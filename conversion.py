# My Conversion Tools
# They are the best conversion tools ever

def dollars2cents(dollars):
   cents = dollars * 100
   return cents

def gallons2liters(gallons):
   liters = gallons * 3.78
   return liters

def hours2minutes(hours):
   minutes = hours * 60
   return hours

